export function isSupportedLocale(value: string | null | undefined): value is Locale {
  return value === 'ko' || value === 'en';
}

export function detectLocale(): Locale {
  const queryLocale = new URLSearchParams(window.location.search).get('lang');
  if (isSupportedLocale(queryLocale)) return queryLocale;

  const storedLocale = window.localStorage.getItem('plainview.locale');
  if (isSupportedLocale(storedLocale)) return storedLocale;

  const languages = navigator.languages?.length ? navigator.languages : [navigator.language];
  return languages.some((language) => language.toLowerCase().startsWith('ko')) ? 'ko' : 'en';
}

export function translate(
  locale: Locale,
  key: TranslationKey,
  values?: Record<string, string | number>
) {
  const template = translations[locale][key] ?? translations.en[key] ?? key;
  if (!values) return template;

  return template.replace(/\{(\w+)\}/g, (_, name) =>
    Object.prototype.hasOwnProperty.call(values, name) ? String(values[name]) : `{${name}}`
  );
}
